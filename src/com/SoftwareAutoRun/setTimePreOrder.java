    private void getDataTable(String tableNo) {
        TableFileControl tb = new TableFileControl();
        TableFileBean bean = new TableFileBean();
        bean = tb.getData(txtTable.getText());
        if (bean.getTcode().equals("null")) {

            MSG.NOTICE("ไม่พบเบอร์โต๊ะ กรุณากรอกใหม่");
        } else {
            txtCustomer.setText(bean.getTCustomer() + " คน ");
            model = (DefaultTableModel) tbShowBalance.getModel();
            tbShowBalance.setShowGrid(true);
            tbShowBalance.setGridColor(Color.gray);
            tbShowBalance.setRowHeight(45);
            int rowCount = model.getRowCount();
            for (int i = 0; i < rowCount; i++) {
                model.removeRow(0);
            }
            BalanceControl balanceControl = new BalanceControl();
            List<BalanceBean> listBalance = balanceControl.getAllBalanceNoVoidSum(tableNo);

            for (BalanceBean balanceBean : listBalance) {
                DecimalFormat QtyIntFmt = new DecimalFormat("#,##0");

                String rEtd = balanceBean.getR_ETD();
                String PName = rEtd + " " + balanceBean.getR_PName();
                String newName1 = getNewPNameWrap(PName);
                String newName2 = getNewPNameWrap(PName);
                String newName3 = getNewPNameWrap(PName);
                String r_type = balanceBean.getR_Type();
                switch (r_type) {
                    case "1":
                        newName2 = "";
                        newName3 = "";
                        break;
                    case "2":
                        newName1 = "";
                        newName3 = "";
                        break;
                    case "3":
                        newName1 = "";
                        newName2 = "";
                        break;

                }
                Object[] input = {
                    QtyIntFmt.format(balanceBean.getR_Quan()),
                    newName1,
                    newName2,
                    newName3,
                    QtyIntFmt.format(balanceBean.getR_Price()),
                    "",
                    balanceBean.getR_PluCode()
                };
                model.addRow(input);

            }
        }
    }

