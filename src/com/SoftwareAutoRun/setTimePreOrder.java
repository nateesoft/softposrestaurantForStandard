    String getNewPNameWrap(String PName) {
        String[] names = new String[2];
        if (PName.length() > 50) {
            names[0] = PName.substring(0, 50);
            names[1] = PName.substring(50, PName.length());
        } else {
            return PName;
        }

        if (names[0] == null) {
            names[0] = "";
        }
        if (names[1] == null) {
            names[1] = "";
        }

        return names[0] + "<br>" + names[1];
    }

